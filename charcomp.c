typedef struct {
  uint16_t start;
  uint16_t end;
} Range;


bool letters = true;
bool symbols = true;
bool digits = true;

Range* range(uint16_t start, uint16_t end) {
  Range* range = (Range*) calloc(1, sizeof(Range));
  range->start = start;
  range->end = end;
  return range;
  // memory leak ignored
}

uint16_t length(Range* range) {
  return range->end - range->start;
}

void copy(uint8_t* src, uint8_t* dst, Range* srange, Range *drange) {
  for(uint16_t i=0; i<length(srange)*8; i++) {
    dst[drange->start*8+i] = src[srange->start*8+i];
  }  
}

void copy_reverse(uint8_t* src, uint8_t* dst, Range* srange, Range *drange) {
  for(uint16_t i=0; i<length(srange)*8; i++) {
    dst[drange->start*8+i] = ~(src[srange->start*8+i]);
  }  
}

void copy_mixed(void) {
  // uppper
  if(letters) copy(input, output, range(65, 65+26), range(1, 27)); // uppercase letters
  if(digits)  copy(input, output, range(48, 58), range(48, 48)); // digits
  if(symbols) copy(input, output, range(0, 1), range(0, 1)); // punctuation @
  if(symbols) copy(input, output, range(27, 48), range(27, 48)); // punctuation up to digits
  if(symbols) copy(input, output, range(58, 64), range(58, 64)); // punctuation after digits
  
  if(letters) copy_reverse(input, output, range(65, 65+26), range(128+1, 128+27)); // uppercase letters
  if(digits)  copy_reverse(input, output, range(48, 58), range(128+48, 128+48)); // digits
  if(symbols) copy_reverse(input, output, range(0, 1), range(128+0, 128+1)); // punctuation @
  if(symbols) copy_reverse(input, output, range(27, 48), range(128+27, 128+48)); // punctuation up to digits
  if(symbols) copy_reverse(input, output, range(58, 64), range(128+58, 128+64)); // punctuation after digits

  // mixed
  if(letters) copy(input, output, range(1, 27), range(256+1, 256+27)); // lowercase letters
  if(letters) copy(input, output, range(64+1, 64+27), range(256+64+1, 256+64+27)); // uppercase letters
  if(digits)  copy(input, output, range(48, 58), range(256+48, 256+58)); // digits
  if(symbols) copy(input, output, range(0, 1), range(256+0, 256+1)); // punctuation @  
  if(symbols) copy(input, output, range(27, 48), range(256+27, 256+48)); // punctuation up to digits
  if(symbols) copy(input, output, range(58, 64), range(256+58, 256+64)); // punctuation after digits
    
  if(letters) copy_reverse(input, output, range(1, 27), range(128+256+1, 128+256+27)); // lowercase letters  
  if(letters) copy_reverse(input, output, range(64+1, 64+27), range(128+256+64+1, 128+256+64+27)); // uppercase letters
  if(digits)  copy_reverse(input, output, range(48, 58), range(128+256+48, 128+256+58)); // digits
  if(symbols) copy_reverse(input, output, range(0, 1), range(128+256+0, 128+256+1)); // punctuation @
  if(symbols) copy_reverse(input, output, range(27, 48), range(128+256+27, 128+256+48)); // punctuation up to digits
  if(symbols) copy_reverse(input, output, range(58, 64), range(128+256+58, 128+256+64)); // punctuation after digits
}

void copy_upper(void) {
  // upper
  if(letters) copy(input, output, range(1, 27), range(1, 27)); // letters
  if(digits)  copy(input, output, range(48, 58), range(48, 48)); // digits  
  if(symbols) copy(input, output, range(0, 1), range(0, 1)); // punctuation @  
  if(symbols) copy(input, output, range(27, 48), range(27, 48)); // punctuation up to digits
  if(symbols) copy(input, output, range(58, 64), range(58, 64)); // punctuation after digits
  if(symbols) copy(input, output, range(94, 95), range(94, 95)); // pi
  
  if(letters) copy_reverse(input, output, range(1, 27), range(128+1, 128+27)); // letters
  if(digits)  copy_reverse(input, output, range(48, 58), range(128+48, 128+48)); // digits  
  if(symbols) copy_reverse(input, output, range(0, 1), range(128, 129)); // punctuation @
  if(symbols) copy_reverse(input, output, range(27, 48), range(128+27, 128+48)); // punctuation up to digits
  if(symbols) copy_reverse(input, output, range(58, 64), range(128+58, 128+64)); // punctuation after digits
  if(symbols) copy_reverse(input, output, range(94, 95), range(128+94, 128+95)); // pi

  // mixed
  if(letters) copy(input, output, range(1, 27), range(256+64+1, 256+64+27)); // letters
  if(digits)  copy(input, output, range(48, 58), range(256+48, 256+58)); // digits  
  if(symbols) copy(input, output, range(0, 1), range(256, 257)); // punctuation @
  if(symbols) copy(input, output, range(27, 48), range(256+27, 256+48)); // punctuation up to digits
  if(symbols) copy(input, output, range(58, 64), range(256+58, 256+64)); // punctuation after digits

  if(letters) copy_reverse(input, output, range(1, 27), range(128+256+64+1, 128+256+64+27)); // letters
  if(digits)  copy_reverse(input, output, range(48, 58), range(128+256+48, 128+256+58)); // digits  
  if(symbols) copy_reverse(input, output, range(0, 1), range(128+256, 128+257)); // punctuation @
  if(symbols) copy_reverse(input, output, range(27, 48), range(128+256+27, 128+256+48)); // punctuation up to digits
  if(symbols) copy_reverse(input, output, range(58, 64), range(128+256+58, 128+256+64)); // punctuation after digits
}

void copy_full(void) {
  copy_upper();
  if(letters) copy(input, output, range(256+1, 256+27), range(256+1, 256+27)); // lowercase letters
  if(letters) copy_reverse(input, output, range(256+1, 256+27), range(128+256+1, 128+256+27)); // lowercase letters
}

void write_rom(char* filename, uint16_t size, uint16_t offset) {

  outfile = stdout;
  if(strcmp(filename, "-") != 0) {
    if((outfile = fopen(filename, "wb")) == NULL) {
      fprintf(stderr, "%s: %s\n", filename, strerror(errno));
      exit(EXIT_FAILURE);
    }
  }
  
  fwrite(output+offset, sizeof(uint8_t), size, outfile);    
  fclose(outfile);
}

void write_pbm(char* filename, uint16_t size, uint16_t offset) {

  uint8_t rows = size/256;

  outfile = stdout;
  if(strcmp(filename, "-") != 0) {
    if((outfile = fopen(filename, "wb")) == NULL) {
      fprintf(stderr, "%s: %s\n", filename, strerror(errno));
      exit(EXIT_FAILURE);
    }
  }
  
  fprintf(outfile, "P4\n");
  fprintf(outfile, "256 %d\n", rows*8);
  
  for(int r=0; r<rows; r++) {
    for(int b=0; b<8; b++) {
      for(int c=0; c<32; c++) {
        fputc(output[r*256+c*8+b+offset], outfile);
      }
    }
  }
  fclose(outfile);
}

